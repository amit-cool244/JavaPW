public class Operators
{
        public static void main(String[] args) 
        {
            int num1=60;
            int num2=20;
            System.out.println(num1 + num2);
            System.out.println(num1 - num2);
            System.out.println(num1 * num2);
            System.out.println(num1 / num2);
            System.out.println(10 % 2);//modulo gives the reminder
            System.out.println(5 % 10);//5 cannot be divided by 10 so the remainder is 5
        }

}